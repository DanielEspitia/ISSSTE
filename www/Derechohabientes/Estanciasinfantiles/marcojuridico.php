<p ><strong>Artículo 31.</strong>Señala la obligación de los mexicanos para que sus hijos reciban educación básica.</p>
<h4 align="justify"> <strong> La ley del ISSSTE, en sus artículos 4, fracción III, inciso d; 195 y 196 fracción IV, establece</strong>:</h4>
<p ><strong>Artículo 4, fracción III, inciso d.</strong>Establece con carácter de obligatorio la presentación de servicio de atención para el bienestar y desarrollo infantil.</p>
<p ><strong>“Artículo 195</strong>. 


El instituto atenderá de acuerdo con esta Ley, a las necesidades básicas del Trabajador y su familia a través de la presentación de servicios que contribuyan al apoyo asistencial, a la protección del poder adquisitivo de sus salarios, con orientación hacia patrones racionales y sanos de consumo.”</p>
<p >“<strong>Artículo 196.</strong>Para los efectos del artículo anterior, el Instituto, de acuerdo con las posibilidades financieras del Fondo de servicios sociales y culturales, proporcionará a precios módicos los servicios sociales siguientes: …IV Servicios de atención para el bienestar y desarrollo infantil, y…” </p>
<h4>Ley General de Educación.</h4>
<p > Regula la educación que imparte la federación, los estados y municipios, establece la estructura educativa y las competencias de cada nivel de gobierno en materia educativa y regula la validez oficial de los estudios y la certificación de conocimientos.</p>
<h4 align="justify"><strong><strong> Ley General de Prestación de Servicios para la Atención, Cuidado y Desarrollo Integral Infantil</strong>.</strong></h4>
<p > Es de orden público, interés social y observancia general en el territorio nacional y tiene por objeto establecer la concurrencia entre la Federación, los Estados, los Municipios, el Distrito Federal y los órganos político-administrativos de sus demarcaciones territoriales, así como la participación de los sectores privado y social, en materia de prestación de servicios para la atención, cuidado y desarrollo integral infantil, garantizando el acceso de niñas y niños a dichos servicios en condiciones de igualdad, calidad, calidez, seguridad y protección adecuadas, que promuevan el ejercicio pleno de sus derechos. </p>
<h4 > <strong> NOM-032-SSA3-2010 Asistencia social. Prestación de servicios de asistencia social para niños, niñas y adolecentes en situaciones de riesgo y vulnerabilidad</strong>.</h4>
<p > Esta norma tiene por objeto establecer las características y los requisitos mínimos que deben observarse en los Establecimientos o Espacios de los sectores público, social y privado que presten servicios de asistencia social a niños, niñas y adolecentes en situaciones de riesgo y vulnerabilidad.</p>
<h4 align="justify"> <strong> NOM-043-SSA2-2005 Servicios básicos de salud. Promoción y educación para la salud en materia alimentaria</strong>.</h4>
<p > Establece los criterios que deberán seguirse para orientar a la población en materia de alimentación, así como los elementos para brindar información homogénea y consistente para coadyuvar al mejoramiento del estado desnutrición de la población y prevenir problemas de salud relacionados con la alimentación.</p>
<h4 align="justify"> <strong> NOM-251-SSA1-2009 Prácticas de Higiene para el proceso de alimentos, bebidas o suplementos alimenticios</strong>.</h4>
<p > Establece los requisitos mínimos de buenas prácticas de higiene que deben observarse en el proceso de alimentos, bebidas o suplementos alimenticios y sus materias primas a fin de evitar su contaminación a lo largo de su proceso.</p>
