<h3>Bienvenidos</h3>
<p > El creciente papel de la mujer en el ámbito laboral, sin dejar de cumplir con su papel en la familia, ha ido de la mano con la necesidad de contar con espacios apropiados para el cuidado y el desarrollo de los hijos e hijas. En 1837 surge la primera guardería en México, para el cuidado de los hijos de comerciantes del Mercado del Volador. En 1917, con la inclusión en la Constitución Política de los Estados Unidos Mexicanos de la educación como un derecho, surgen los Hogares Infantiles.</p>
<p > En 1941 se inaugura en la Dirección General de Pensiones Civiles y de Retiro, precursora del ISSSTE, la primera guardería para hijos de trabajadoras al servicio del Estado, en la que se atiende a 3 niños en las instalaciones de la propia Dirección General. En la década de los 70 se adopta el nombre de Estancias de Bienestar Infantil, ya con un notorio reconocimiento de la importancia de la etapa formativa en la primera infancia y se incorpora una perspectiva educativa a la atención brindada a los niños.</p>
<p > Posteriormente, debido a la necesidad de que las trabajadoras desempeñaran su jornada laboral con la confianza de que sus hijos e hijas estarían en un lugar seguro, las dependencias y entidades pusieron a disposición de ellas las Estancias de Bienestar Infantil, las cuales eran administradas por las propias dependencias.</p>
<p > A partir de 1983, fecha en que la Ley del ISSSTE recoge la obligación del Estado de brindar el servicio de estancias infantiles, se aseguran espacios apropiados para la protección y desarrollo de los hijos menores de seis años de las madres trabajadoras.</p>
<p > Con el propósito de dar orden y lograr una mayor eficiencia, en 1984 el ISSSTE asume la rectoría del servicio de estancias que daban las Secretarías de Salud, Marina, Comunicaciones y Transportes, Comercio y Fomento Industrial, Programación y Presupuesto, Hacienda y Crédito Público, Energía, Minas e Industria Paraestatal y Turismo. A partir de 1989 se adopta el nombre de Estancias para el Bienestar y Desarrollo Infantil.</p>
<p > Hoy en día existen 122 estancias propias, 48 en el D.F. y 74 en el interior del país, en las que se da atención a 18,945 infantes. Adicionalmente, para estar en posibilidades de atender una demanda cada vez mayor, a partir de 1999, con base en la “Ley de Adquisiciones, Arrendamientos y Servicios del Sector Público”, se pone en marcha el “Proyecto Nacional de Estancias de Participación Social”, que permite no sólo atender a un mayor número de niños, sino también hacerlo en muchos más lugares. En 2014 el Instituto cuenta con 119 estancias de contratadas, 5 estancias como organizaciones de la sociedad civil, en las que se atiende a un total de 8,592 infantes. En total, entre estancias propias, contratadas y sociedad civil, existen en abril del 2014, 246 estancias con 27,537 niños atendidos.</p>
<p > La operación del servicio es objeto de constante supervisión por parte de la Dirección de Prestaciones Económicas, Sociales y Culturales; de las Delegaciones Estatales y Regionales; de la Comisión de Vigilancia del ISSSTE; de las dependencias estatales y municipales como Protección Civil, Secretaría de Educación Pública y Sector Salud, quienes permanentemente están al tanto del cumplimiento puntual de las leyes y las normas aplicables.</p>
<p > Para cumplir en materia de transparencia, pero sobre todo por la convicción del Instituto de poner al alcance de los derechohabientes y de la ciudadanía en general el máximo de información posible, es que se ofrece esta página electrónica con la información de nuestras EBDIs.</p>
