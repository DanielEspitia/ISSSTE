    <p align="right">Xalapa, Ver., a 16 de julio de 2014.</p>
    
    <div align="justify">
<blockquote>
<ul>
<li class="primera">Iniciará programa intensivo para abatir el rezago en estancias infantiles, anuncia el Director General del Instituto, Sebastián Lerdo de Tejada.</li>
<li class="ultima">Seis mil 405 millones de pesos se destinarán al pago de pensiones y 130 mdp en obra pública.</li>
</ul>
</blockquote>
<p>La derrama económica agregada del ISSSTE para el estado de Veracruz en 2014 es de nueve mil 400 millones de pesos, anunció el Director General del Instituto, Sebastián Lerdo de Tejada, en su gira de trabajo por Xalapa.</p>
<p>Durante la entrega simbólica de 600 créditos personales, y ante un auditorio aproximado de 800 personas, así como representantes sindicales como la FTSTE, FEDESSP, SNTE, SNTISSSTE e integrantes de su cuerpo directivo, detalló que de ese presupuesto, seis mil 405 millones de pesos se destinarán al pago de cerca de 46 mil pensionados y jubilados que existen en la entidad.</p>
<p align="center"><img src="../../images/photos/Comunicados2014/Com_26_img_1.jpg" alt="" border="0" width="580"></p>
<p>Además, indicó que mil 700 millones de pesos serán para créditos de vivienda FOVISSSTE y en materia de obra pública se invertirán 130 millones de pesos, así como 50 millones adicionales para mejora de instalaciones de la institución, por ejemplo áreas de primer contacto e impermeabilización, entre otras.</p>
<p>En tanto, para el rubro de créditos personales, señaló que la entidad contará con 700 millones de pesos en lo que resta del año.</p>
<p align="center"><img src="../../images/photos/Comunicados2014/Com_26_img_2.jpg" alt="" border="0" width="580"></p>
<p>Ante el secretario del Trabajo del Gobierno del Estado, Marco Antonio Aguilar Yunes, quien asistió en representación del Gobernador Javier Duarte de Ochoa, así como del Presidente Municipal, Américo Zúñiga Martínez; el titular del ISSSTE, ratificó el compromiso del organismo a su cargo para mejorar permanentemente todos los servicios, por lo que recordó la reciente cancelación del pase de vigencia presencial para jubilados y pensionados, “porque es inaceptable obligarlos a asistir dos veces al año a las oficinas. Esa es nuestra obligación y tendremos mecanismos de verificación permanente”.</p>
<p align="center"><img src="../../images/photos/Comunicados2014/Com_26_img_3.jpg" alt="" border="0" width="580"></p>
<p>En congruencia con la mejora de servicios, Lerdo de Tejada se comprometió ante los derechohabientes a abatir el rezago en las Estancias para el Bienestar y Desarrollo Infantil (EBDI) locales. “Vamos a hacer un programa intensivo para mejorarlas porque no puede haber ni un solo niño o niña veracruzano o veracruzana que quiera entrar a una de ellas y que no haya lugar. En consecuencia, cero rezago en estancias infantiles en Veracruz, ese es el compromiso que quiero asumir frente a ustedes”.</p>
<p align="center"><img src="../../images/photos/Comunicados2014/Com_26_img_4.jpg" alt="" border="0" width="580"></p>
<p>Destacó que todas estas acciones son parte de la obligación del ISSSTE para otorgar calidad y calidez, es decir para “hacerle la vida más sencilla a la gente. La instrucción del Presidente Enrique Peña Nieto para quienes trabajamos con él, es muy concreta: soluciones específicas, trámites más sencillos y transparencia absoluta en el manejo de los recursos”, resumió.</p>
<p>Por su parte, el representante del Gobernador, Marco Antonio Aguilar Yunes, agradeció la colaboración del Instituto en la implementación de las políticas en favor de los trabajadores al servicio del Estado, y remarcó que los créditos personales significarán una derrama económica de más de 40 millones de pesos.</p>
<p align="center"><img src="../../images/photos/Comunicados2014/Com_26_img_5.jpg" alt="" border="0" width="580"></p>
<p>Tras la entrega de créditos, el Director General presidió la Sesión Extraordinaria del Consejo Consultivo, en la que el Delegado estatal Renato Alarcón Guevara, presentó un informe de actividades, donde subrayó las obras de ampliación y remodelación en la Clínica de Medicina Familiar (CMF) de Tuxpan y en la Clínica Hospital de Xalapa, además de la construcción de la nueva CMF de Córdoba, obras que terminarán en diciembre y que involucran recursos por más de 187 millones de pesos en beneficio de cerca de medio millón de derechohabientes veracruzanos.</p>
<p align="center"><img src="../../images/photos/Comunicados2014/Com_26_img_6.jpg" alt="" border="0" width="580"></p>
<p>Al respecto, el titular del ISSSTE invitó al cuerpo directivo de la Delegación a recuperar el sentido de urgencia, es decir hacer las cosas bien y rápido, y refrendó el respaldo absoluto a los trabajadores del Instituto para poder realizar, en futuras visitas a la entidad, recorridos por las obras que se llevan a cabo en estas regiones.</p>
<p>La sesión del Consejo fue clausurada por el representante del Gobernador, Marco Antonio Aguilar, y asistieron los secretarios generales de la FSTSE, SNTE y FEDESSP e integrantes del cuerpo directivo del ISSSTE.</p>
