<div align="justify">
<blockquote>
<ul>
  <li class="primera"> En gira por el Estado de México entrega de manera simbólica mil 500 préstamos personales a derechohabientes del Instituto. </li>
  <li class="ultima"> Reitera que en el Plan Nacional de Infraestructura está incluida la construcción de un nuevo Hospital Regional para esta entidad. </li>
  </ul>
</blockquote>
<p> En 2014 la derrama económica agregada del ISSSTE para el Estado de México es de 14 mil 937 millones de pesos, informó el Director General, Sebastián Lerdo de Tejada, luego de hacer entrega simbólica de mil 500 créditos a derechohabientes mexiquenses. </p>
<p> Asimismo, remarcó que el Plan Nacional de Infraestructura incluye la construcción de un nuevo Hospital Regional para esta entidad, y que son parte de las acciones que realiza el Instituto para beneficiar a los derechohabientes y a sus familias. </p>
<p align="center"><img src="../../images/photos/Comunicados2014/Com_28_img_1.jpg" alt="" border="0" width="580"> </p>
<p> Acompañado por integrantes de su cuerpo directivo y ante representantes de organizaciones sindicales como el SNTISSSTE, la FEDESSP, la FSTSE y el SNTE, en su visita por el Estado de México, el titular del ISSSTE destacó, además, que del monto total anunciado, ocho mil 198 millones de pesos serán para el pago pensiones de jubilados y pensionados.</p>
<p> En presencia del representante del Gobierno mexiquense, César Gómez Monge, y de la presidenta municipal de Toluca, Martha Hilda González Calderón, el Director General subrayó que parte de las instrucciones del Presidente de la República no es sólo la coordinación transversal con las instituciones para mejorar los servicios, "es la coordinación plena con los órdenes de gobierno, con el Gobernador y su equipo de trabajo, así como con la señora y los señores presidentes municipales". </p>
<p align="center"><img src="../../images/photos/Comunicados2014/Com_28_img_2.jpg" alt="" border="0"> </p>
<p> Durante su intervención, el también Secretario de Salud estatal, Gómez Monge, reconoció el trabajo que realiza el ISSSTE, "porque hemos visto que la atención médica está mejorando, dando pasos firmes y ampliando su infraestructura para dar servicios con mayor calidad". Además, dijo que los créditos otorgados a los derechohabientes mexiquenses ayudarán de manera importante a la economía del estado. </p>
<p> Por su parte, el presidente del CEN del Sindicato Nacional de Trabajadores del ISSSTE, Luis Miguel Victoria Ranfla, externó el apoyo de los trabajadores a la Dirección General, en respuesta a la política que ha implementado para laborar en equipo y porque está moviendo al Instituto en beneficio de todos. </p>
<p align="center"><img src="../../images/photos/Comunicados2014/Com_28_img_3.jpg" alt="" border="0" width="580"> </p>
<p> Destacó la reciente autorización para este organismo de 10 mil nuevas plazas por parte de la Secretaría de Hacienda y Crédito Público, producto de la suma de voluntades. "Con el apoyo del Presidente de la República, con el apoyo del Secretario de Hacienda, del Director General del ISSSTE, de su equipo y del líder nacional de la FSTSE, habremos de conseguir más recursos para el Instituto. Debemos elevar la calidad y calidez de nuestros servicios y para ello necesitamos el número suficiente de trabajadores", finalizó. </p>
<p align="center"><img src="../../images/photos/Comunicados2014/Com_28_img_4.jpg" alt="" border="0" width="580"> </p>
<p> También en su gira de trabajo por esta ciudad capital, el titular del ISSSTE presidió la tercera Sesión Ordinaria del Consejo Consultivo Delegacional, en la que el delegado José Edgar Naime Libién, rindió un informe de actividades en el que agradeció el respaldo del Instituto para avanzar en las obras de remodelación del Hospital General de Toluca y en la construcción de la nueva unidad hospitalaria de Cuautitlán Izcalli. </p>
<p align="center"><img src="../../images/photos/Comunicados2014/Com_28_img_5.jpg" alt="" border="0" width="580"> </p>
<p> Al respecto, Lerdo de Tejada los exhortó a continuar trabajando de manera coordinada y a atender juntos las instrucciones del Presidente de la República, que son, también, reiteró “hacer más con menos, para garantizar a la derechohabiencia servicios de calidad y calidez”.</p>
<p> A la Sesión asistió la secretaria técnica del Consejo Estatal de Salud, Cristina Chaparro Mercado, en representación del Gobernador Eruviel Ávila Villegas. </p>
