<p>Al recibir hoy la visita del presidente de la Corte Interamericana de Derechos Humanos (CIDH), Humberto Antonio Sierra Porto, el Director General del ISSSTE, Sebastián Lerdo de Tejada, dijo que el Instituto se transforma para brindar una mejor seguridad social y más digna a sus derechohabientes.</p>
<p>Agregó que “es la primera institución pública mexicana que incorpora a su Estatuto Orgánico y todos sus Reglamentos la obligación de sus servidores públicos de garantizar, proteger y promover los derechos humanos de los derechohabientes, conforme lo establece la reforma constitucional de 2011”.</p>

<p align="center"><img src="../../images/photos/Comunicados2014/05-08-14/Com_30_img_1.jpg" alt="" border="0" width="580"></p>

<p>En la reunión, el titular del Instituto de Seguridad y Servicios Sociales de los Trabajadores del Estado, acompañado por el Director Jurídico, Salvador Mikel Rivera y del investigador de la UNAM, Edgar Corzo, ratificó su compromiso en materia de derechos humanos y señaló que en el Estatuto, que entró en vigor el 18 de junio, se establece la centralidad del derechohabiente y se reconocen no solamente los derechos de madres sino ahora también de los padres trabajadores de inscribir a sus hijos en las estancias infantiles; que los menores sean atendidos sin discriminación alguna y se brinde atención a niños con discapacidad.</p>

<p>Además, añadió el Director General, también se reconocen los derechos de cónyuges y concubinos sin importar su sexo; se protege a grupos vulnerables como menores, mujeres y adultos mayores, y se establecen responsabilidades en caso de que se vulnere alguno de estos derechos.</p>

<p align="center"><img src="../../images/photos/Comunicados2014/05-08-14/Com_30_img_2.jpg" alt="" border="0" width="580"></p>

<p>Incluso, la nueva norma reorganiza áreas y recursos para, sin causar costos adicionales a la Institución, mejorar la calidad de la atención que presta el ISSSTE, para lo cual, se estableció una Unidad de Mediación que habrá de contribuir a un mayor respeto a los derechos humanos de los derechohabientes.</p>

<p>Asimismo, dijo Lerdo de Tejada, se ha intensificado la capacitación en la materia y el combate a la discriminación, y se decidió aceptar todas las recomendaciones de la Comisión Nacional de los Derechos Humanos, lo que se ha traducido en una reducción significativa, en 2013, de las recomendaciones de la CNDH, las cuales disminuyeron de 14 a 3, y que no se registre ninguna en lo que va de 2014.</p>

<p>Son los avances alcanzados durante los primeros 18 meses del gobierno que encabeza el Presidente Enrique Peña Nieto, subrayó el Director General del ISSSTE, quien ratificó su compromiso de colaborar con instituciones como la Corte Interamericana de los Derechos Humanos, a fin de impulsar una cultura de los derechos humanos y un servicio público, en la seguridad social comprometido con su garantía.</p>

