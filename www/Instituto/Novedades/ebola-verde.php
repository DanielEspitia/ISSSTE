  <h1>PREGUNTAS MÁS FRECUENTES SOBRE  </h1>
  <h1>LA ENFERMEDAD POR EL VIRUS DEL ÉBOLA </h1>
   <br>
<h2 style="color:#060">¿Qué es la enfermedad provocada por el virus del Ébola? </h2>
<p>El virus del Ébola causa una enfermedad aguda grave que se caracteriza por fiebre, dolor de cabeza, dolor muscular, dolor de garganta, diarrea, vómito, dolor de estómago o enrojecimiento de los ojos; en algunos casos se presentan hemorragias internas y externas,
llegando a ser fatal frecuentemente. </p>
<br>

<h2 style="color:#060">¿Es una enfermedad nueva?</h2>
<p>No, se detectó por primera vez en 1976 en dos brotes epidémicos ocurridos en las regiones de África: Nzara, Sudán y Yambuku y República Democrática del Congo. La aldea en la que se produjo el segundo brote se ubica cerca del río Ébola, que da nombre al virus. </p>

<p>Desde el 23 de marzo del 2014 y a la fecha, existe un nuevo brote de esta enfermedad en Guinea, Liberia, Sierra Leona y Nigeria, países de
África. </p>
<br>

<h2 style="color:#060">¿Existe riesgo de que la enfermedad llegue a México? </h2>
<p>La probabilidad de que esta enfermedad se presente en México es extremadamente baja. </p>
<br>

<h2 style="color:#060">¿Cómo se transmite?</h2>
<p>El virus es transmitido al ser humano por animales salvajes infectados y se propaga en las poblaciones humanas por transmisión de persona a persona, al tener contacto directo con sangre, secreciones y líquidos corporales de las personas infectadas con el virus. </p>

<p>El virus del Ébola <b>no se transmite a través del aire, agua o alimentos contaminados.</b> </p>
<br>

<h2 style="color:#060">¿Quiénes tienen mayor riesgo de contraer la enfermedad? </h2>
<p>Puede afectar a mujeres y hombres de todas las edad es. Sin embargo, el riesgo es mayor para el personal médico, paramédico y cuidadores de
personas enfermas o sospechosas de tener el Virus del Ébola. </p>
<br>

<h2 style="color:#060">¿Cuál es el período de incubación?</h2>
<p>El período de incubación es de 2 a 21 días.</p>
<p>Las personas infectadas pueden transmitir el virus mientras esté presente en sangre, secreciones y en líquido seminal (semen) hasta 61 días después de la aparición de la enfermedad. </p>
<br>

<h2 style="color:#060">¿Cómo se realiza el diagnóstico?</h2>
<p>La infección por virus del Ébola sólo puede diagnosticarse a través de pruebas de laboratorio, tomando muestras de sangre. </p>
<br>

<h2 style="color:#060">¿Existe tratamiento o vacuna para la enfermedad del virus del Ébola? </h2>
<p>Desafortundamente, no existe un tratamiento específico o vacuna para esta enfermedad.<br>
Los cuidados médicos son: hidratación, control de la fiebre, y control de la presión arterial. </p>
<br>

<h2 style="color:#060">¿Qué medidas preventivas deben realizarse? </h2>
<blockquote>
  <ul type="disc">
    <li> Medidas higiénicas, principalmente lavado de manos.</li>
    <li>Evitar el contacto con sangre y fluidos corporales.</li>
     <li>Evitar contacto con animales que pudieran estar enfermos.</li>
     <li>Evitar manejo de cadáveres humanos o de animales que pudieran estar contaminados.</li>
     <li>Utilización de equipo de protección personal. </li>
  </ul>
  </blockquote>
<br>

<h2 style="color:#060">¿Se puede viajar a Guinea, Liberia, Sierra Leona y Nigeria?</h2>
<p><b>Se recomienda evitar viajes a Guinea, Liberia, Sierra Leona y Nigeria, </b>debido a la evolución del brote por el Virus de Ébola. </p>
<br>

<h2 style="color:#060">¿Qué se debe hacer en caso necesario de viajar a Guinea, Liberia, Sierra Leona y Nigeria?</h2>
<p>Es importante realizar estrictas medidas de higiene, además de: </p>
<blockquote>
  <ul type="disc">
   <li>Evitar el contacto con sangre y fluidos corporales de personas infectadas. </li>
   <li>No tocar utensilios o instrumentos que puedan haber estado en contacto con sangre o fluidos corporales de una persona infectada.</li>   
   <li>Evitar el contacto con animales que pudieran estar enfermos (monos, chimpancés, antílopes y murciélagos silvestres).</li>
   <li>Evitar contacto y manejo con cadáveres de humanos o animales que pudieran haber estado infectados.</li>
   <li>Si es personal médico o paramédico y no cuenta con la capacitación y equipo de protección personal adecuada, evitar atender a personas enfermas o sospechosas de tener la enfermedad.</li>
  <li>Mantenerse atento a los medios de comunicación dura nte su estancia en estos países y realizar siempre las medidas preventivas. </li>
  </ul>
</blockquote>
<br>

<h2 style="color:#060">¿Qué hacer si durante el viaje se presentan los síntomas de la enfermedad?</h2>
<p>Se debe buscar <b>atención médica de inmediato.</b> </p>
<br>

<h2 style="color:#060">¿Qué hacer si después de viajar a estos países se presentan los síntomas de la enfermedad? </h2>
<blockquote>
<ul type="disc">
<li>Si los síntomas se presentan durante el viaje de regreso, se debe avisar al asistente de vuelo antes de su arribo o al oficial de los ser
vicios de migración y/o sanidad internacional al salir del vuelo, para facilitar la atención médica.</li>
<li>En caso de presentar los síntomas dentro de los primeros 21 días del regreso del viaje, se debe buscar atención médica de inmediato.</li>
</ul>
</blockquote>
