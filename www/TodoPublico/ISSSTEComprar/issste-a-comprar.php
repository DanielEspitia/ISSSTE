 <h2>El ISSSTE va a Comprar</h2>

 <br><br>


 <p>Como ustedes saben, el <b>Instituto de Seguridad y Servicios Sociales de los Trabajadores al Servicio del Estado</b>, enfrenta grandes retos, que afrontamos mejorando la administración, transparentando la gestión y estableciendo premisas formales que apuntan a la reforma integral del Instituto.</p>

<p>Como parte relevante de esta importante tarea, en el mes de febrero de 2001 hicimos público nuestro compromiso tanto de transparentar la gestión como de rendir cuentas, prácticas cotidianas en el <b>ISSSTE</b>.</p>

<p>Establecimos entonces acciones concretas que se han visto incorporadas en los programas operativos de transparencia y combate a la corrupción cuyo seguimiento de resultados es evaluado trimestralmente por la Secretaría de la Función Pública y los Órganos Internos de Control.</p>

<p>En esta dinámica, el esfuerzo a nivel federal se realiza para erradicar el cohecho en las prácticas institucionales, cobra especial significado y ocupa nuestra puntual atención.
Estamos comprometidos en esta lucha y para ello fortaleceremos los mecanismos orientados a combatir el cohecho explorando en principio mecanismos transparentes, equitativos y escrupulosamente monitoreados de participación en los procesos licitatorios que el <b>ISSSTE</b> realiza.</p>

<p>Requerimos, sin embargo, de la participación de ustedes para hacernos llegar comentarios, sugerencias, quejas y denuncias específicas, que nos permitan avanzar en este proceso de combatir las prácticas irregulares como el cohecho. Pongo a su disposición para ser atendidos en forma personal por el suscrito, el correo electrónico: <a href="mailto:sebastian.lerdodetejada@issste.gob.mx" class="tooltip_arriba">sebastian.lerdodetejada@issste.gob.mx</a> con la seguridad de que en cada caso procederemos en consecuencia.</p>

<p>Seguros de que debemos pasar de la parte conceptual a la instrumental, acompañamos un <a href="/index.php/compromisos-issste-provedores" target="_blank" class="tooltip_arriba">decálogo de compromisos</a> que queremos hacer explícitos ante ustedes, para disponer del punto de partida básico y efectuar los señalamientos respectivos.</p>

<p>Finalmente, reiterar que el ISSSTE se encuentra en un proceso de transformación que pretendemos arraigar en la práctica cotidiana, para el cual, a manera de información, acompañamos un breve texto elaborado por la Secretaría de la Función Pública, que aporta mayores precisiones y posibilidades de acceso a la página Web correspondiente.
Reciba nuestra consideración, nuestro reconocimiento y aprecio.</p>

<h2>Atentamente</h2>
<h3> El Director General</h3>
